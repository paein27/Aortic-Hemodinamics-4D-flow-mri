function plot_pwv_results(geometry, pwv)
%PLOT_PWV_RESULTS Graficos de ajuste de PWV.
%   Dibuja los ajustes tiempo-distancia de TTP y TTF (plot_pwv_fit) y el
%   ajuste delay-distancia de la cross-correlation global (Markl).

    section_arc_positions_m = geometry.section_arc_positions_m;
    xcor_reference_section_index = pwv.xcor_reference_section_index;

    plot_pwv_fit( ...
        section_arc_positions_m, pwv.TTP_s, pwv.fit_TTP, ...
        pwv.PWV_TTP_mps, pwv.R2_TTP, 'PWV por TTP', 'TTP [s]');
    plot_pwv_fit( ...
        section_arc_positions_m, pwv.TTF_s, pwv.fit_TTF, ...
        pwv.PWV_TTF_mps, pwv.R2_TTF, 'PWV por TTF', 'TTF [s]');

    relative_distance_xcor_m = ...
        section_arc_positions_m(:) - ...
        section_arc_positions_m(xcor_reference_section_index);
    valid_xcor_plot_mask = ...
        isfinite(relative_distance_xcor_m) & isfinite(pwv.Delay_XCor_s);
    x_fit = linspace( ...
        min(relative_distance_xcor_m(valid_xcor_plot_mask)), ...
        max(relative_distance_xcor_m(valid_xcor_plot_mask)), ...
        200);
    y_fit = polyval(pwv.fit_XCor, x_fit);

    figure('Name', 'PWV por XCor')
    plot( ...
        relative_distance_xcor_m(valid_xcor_plot_mask), ...
        pwv.Delay_XCor_s(valid_xcor_plot_mask), ...
        'o', 'MarkerSize', 7, 'LineWidth', 1.5)
    hold on
    plot(x_fit, y_fit, '-', 'LineWidth', 1.8)

    xlabel('Distancia sobre centerline [m]')
    ylabel('Delay relativo [s]')
    title(sprintf( ...
        'PWV por XCor global = %.3f m/s | R2 = %.3f', ...
        pwv.PWV_XCor_mps, pwv.R2_XCor))
    legend({'Delay por corte', 'Ajuste lineal'}, 'Location', 'best')
    grid on
    hold off
end
