function [PWVSummary, PWVPlaneSummary] = ...
    report_pwv_results(data, geometry, pwv, visualization_phase_index)
%REPORT_PWV_RESULTS Imprime resultados de PWV y arma las tablas resumen.
%   Construye PWVSummary (PWV, pendiente, R2 y planos validos por metodo) y
%   PWVPlaneSummary (tiempos caracteristicos por corte), y escribe el bloque
%   de resultados por consola. Devuelve ambas tablas.

    dataset_label = data.dataset_label;
    time_vector_seconds = data.time_vector_seconds;

    number_of_aortic_sections = geometry.number_of_aortic_sections;
    section_arc_positions_m = geometry.section_arc_positions_m;
    section_arc_positions_mm = geometry.section_arc_positions_mm;
    section_I_urbina = geometry.section_I_urbina;
    section_IV_urbina = geometry.section_IV_urbina;

    xcor_reference_section_index = pwv.xcor_reference_section_index;
    TTP_s = pwv.TTP_s;
    TTF_s = pwv.TTF_s;
    Delay_XCor_s = pwv.Delay_XCor_s;
    Delay_Urbina_XCor_s = pwv.Delay_Urbina_XCor_s;

    valid_ttp_section_count = nnz(isfinite(TTP_s));
    valid_ttf_section_count = nnz(isfinite(TTF_s));
    valid_xcor_section_count = nnz(isfinite(Delay_XCor_s));
    valid_urbina_xcor_section_count = nnz(isfinite(Delay_Urbina_XCor_s));

    PWVSummary = table( ...
        ["TTP"; "TTF"; "XCor"; "XCor_Urbina_I_IV"], ...
        [pwv.PWV_TTP_mps; pwv.PWV_TTF_mps; pwv.PWV_XCor_mps; ...
            pwv.PWV_Urbina_XCor_mps], ...
        [pwv.fit_TTP(1); pwv.fit_TTF(1); pwv.fit_XCor(1); ...
            pwv.fit_Urbina_XCor(1)], ...
        [pwv.R2_TTP; pwv.R2_TTF; pwv.R2_XCor; pwv.R2_Urbina_XCor], ...
        [valid_ttp_section_count; ...
            valid_ttf_section_count; ...
            valid_xcor_section_count; ...
            valid_urbina_xcor_section_count], ...
        [number_of_aortic_sections; ...
            number_of_aortic_sections; ...
            number_of_aortic_sections; ...
            2], ...
        ["TTP all sections"; ...
            "TTF all sections: Markl 20-80% peak-flow foot"; ...
            "XCor Markl: all sections vs reference section"; ...
            "Urbina/Sotelo: XCor between sections I and IV"], ...
        'VariableNames', { ...
            'Method', ...
            'PWV_mps', ...
            'Slope_s_per_m', ...
            'R2', ...
            'ValidPlanes', ...
            'TotalPlanes', ...
            'Notes'});

    PWVPlaneSummary = table( ...
        (1:number_of_aortic_sections)', ...
        section_arc_positions_m(:), ...
        TTP_s(:), ...
        TTF_s(:), ...
        Delay_XCor_s(:), ...
        'VariableNames', {'Plane', 's_m', 'TTP_s', 'TTF_s', 'XCor'});

    fprintf('\n============================================================\n')
    fprintf('RESULTADOS %s\n', upper(dataset_label))
    fprintf('============================================================\n')
    fprintf('Numero de fases temporales: %d\n', numel(time_vector_seconds))
    fprintf('Tiempo inicial: %.6f s\n', time_vector_seconds(1))
    fprintf('Tiempo final: %.6f s\n', time_vector_seconds(end))
    fprintf('dt medio: %.6f s\n', mean(diff(time_vector_seconds)))
    fprintf('\nNumero de cortes: %d\n', number_of_aortic_sections)
    fprintf('Secciones Urbina/Sotelo I-IV: %d-%d\n', ...
        section_I_urbina, section_IV_urbina)
    fprintf('Referencia XCor Markl global: %d\n', ...
        xcor_reference_section_index)
    fprintf(['PWV XCor global = %.4f m/s | slope = %.6f s/m | ', ...
        'R2 = %.4f | planos validos = %d\n'], ...
        pwv.PWV_XCor_mps, pwv.fit_XCor(1), pwv.R2_XCor, valid_xcor_section_count)
    fprintf(['PWV XCor Urbina/Sotelo I-IV = %.4f m/s | ', ...
        'distancia = %.1f mm | delay = %.6f s | corr = %.4f\n'], ...
        pwv.PWV_Urbina_XCor_mps, pwv.urbina_pair_distance_m*1000, ...
        pwv.urbina_pair_delay_s, pwv.urbina_pair_maximum_correlation)
    fprintf('Separacion media: %.1f mm\n', mean(diff(section_arc_positions_mm)))
    fprintf('Fase visualizada: %d\n', visualization_phase_index)
    fprintf('\nResumen de PWV:\n')
    disp(PWVSummary)
    fprintf('\nTiempos caracteristicos por corte:\n')
    disp(PWVPlaneSummary)
end
